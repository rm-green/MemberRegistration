    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        Member temp = new Member();
        ArrayList currentList = con.getMembers();
        String s = IDField.getText();
        int target = Integer.parseInt(s);
        int i;
        for(i = 0; i<currentList.size(); i++){
            temp = (Member)currentList.get(i);
            if(temp.getID() == target){
                currentList.remove(i);
                break;
            }
        }
        if(!firstNameField.getText().trim().isEmpty()){
            temp.setFirstName(firstNameField.getText());
        }
        if(!lastNameField.getText().trim().isEmpty()){
            temp.setLastName(lastNameField.getText());
        }
        if(!emailField.getText().trim().isEmpty()){
            temp.setEmail(emailField.getText());
        }
        if(!contactNumberField.getText().trim().isEmpty()){
            temp.setContact(contactNumberField.getText());
        }
        if(silverRadioButton.isSelected()){
            temp.setMembership("silver");
        }
        else if(goldRadioButton.isSelected()){
            temp.setMembership("gold");
        }
        else if(platinumRadioButton.isSelected()){
            temp.setMembership("platinum");
        }
        
        currentList.add(i, temp);
        con.setMembers(currentList);
        result.setText("Member updated");
        ClearFields();
    }//GEN-LAST:event_updateButtonActionPerformed

