    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        ArrayList currentList = con.getMembers();
        String s = IDField.getText();
        int target = Integer.parseInt(s);
        boolean found = false;
        int i = 0;
        Member temp;
        while(!found){
            try{
                temp = (Member)currentList.get(i);
            }
            catch (IndexOutOfBoundsException e){
                result.setText("Member not found");
                return;
            }
            if(temp.getID() == target){
                currentList.remove(i);
                result.setText("Member ID " + target + " deleted.");
                found = true;
            }
            i++;
        }
        con.setMembers(currentList);
    }//GEN-LAST:event_deleteButtonActionPerformed

