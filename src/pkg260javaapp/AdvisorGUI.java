    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        String aID, aFirstName, aLastName, aEmail, aContact;
        String error = "Please enter all details";
        if(IDField.getText().trim().isEmpty()){JOptionPane.showMessageDialog(this,error); return;}
        else{aID = IDField.getText();}
        if(firstNameField.getText().trim().isEmpty()){JOptionPane.showMessageDialog(this,error); return;}
        else{aFirstName = firstNameField.getText();}
        if(lastNameField.getText().trim().isEmpty()){JOptionPane.showMessageDialog(this,error); return;}
        else{aLastName = lastNameField.getText();}
        if(emailField.getText().trim().isEmpty()){JOptionPane.showMessageDialog(this,error); return;}
        else{aEmail = emailField.getText();}
        if(contactNumberField.getText().trim().isEmpty()){JOptionPane.showMessageDialog(this,error); return;}
        else{aContact = contactNumberField.getText();}
        String aMembership = "";
        if(silverRadioButton.isSelected()){
            aMembership = "silver";
        }
        else if(goldRadioButton.isSelected()){
            aMembership = "gold";
        }
        else if(platinumRadioButton.isSelected()){
            aMembership = "platinum";
        }
        else {
            JOptionPane.showMessageDialog(this,error);
            return;
        }
        java.util.Date today = new java.util.Date();
        long now = today.getTime();
        Date uRegDate = new Date(now);
        int id = Integer.parseInt(aID);
        
        con.register(id, aFirstName, aLastName, aEmail, aContact, aMembership, uRegDate);
        
        String data = "New member is added.";
        ClearFields();
        
        result.setText(data);
        
    }//GEN-LAST:event_addButtonActionPerformed

